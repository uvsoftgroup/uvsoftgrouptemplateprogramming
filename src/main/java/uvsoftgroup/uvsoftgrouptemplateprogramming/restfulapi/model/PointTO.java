package uvsoftgroup.uvsoftgrouptemplateprogramming.restfulapi.model;

public class PointTO {
	  private final float x;
	  private final float y;

	  public PointTO(float x, float y) {
	    this.x = x;
	    this.y = y;
	  }

	  public float getX() {
	    return x;
	  }

	  public float getY() {
	    return y;
	  }
}
